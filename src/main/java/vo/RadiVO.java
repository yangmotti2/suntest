package vo;

public class RadiVO {
	private int idx, direct_radiation;
	private String time;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getDirect_radiation() {
		return direct_radiation;
	}
	public void setDirect_radiation(int direct_radiation) {
		this.direct_radiation = direct_radiation;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
}
